   public void generateWorld() {
       for(int j = world.length-1;j != -1; j--){
           for(int i = world[j].length-1;i != -1; i--){
               if(world[j][i] != 0){
                   generateObject(world[j][i], i*40, j*40);
               }
           }
       }
   }

   public void generateObject(int obj, int x, int y) {
       Actor object = null;
       if(obj == 1){
           object = new Barrel();
       }else if(obj == 2){
           object = Player.get();
       }else if(obj == 3){
           object = new Bomb();
       }else if(obj == 4){
           object = new Barrel();
       }

       if(object != null){
           addObject(object, 20+x, 20+y);
       }
   }
